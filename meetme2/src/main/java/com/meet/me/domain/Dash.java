package com.meet.me.domain;

public class Dash {
	private int category_num;
	private int[] cat_nums;
	private String category_name;
	private int[] interests_count_list;
	
	
	public int getCategory_num() {
		return category_num;
	}
	public void setCategory_num(int category_num) {
		this.category_num = category_num;
	}
	public String getCategory_name() {
		return category_name;
	}
	public void setCategory_name(String category_name) {
		this.category_name = category_name;
	}
	public int[] getInterests_count_list() {
		return interests_count_list;
	}
	public void setInterests_count_list(int[] interests_count_list) {
		this.interests_count_list = interests_count_list;
	}
	public int[] getCat_nums() {
		return cat_nums;
	}
	public void setCat_nums(int[] cat_nums) {
		this.cat_nums = cat_nums;
	}

	
	
}
